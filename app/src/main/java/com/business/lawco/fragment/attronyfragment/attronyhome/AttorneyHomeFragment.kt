    // This Function is used for get all request of consumer
    private fun getAllRequest(latitude: Double, longitude: Double) {
        Log.e("Latitude", latitude.toString())
        Log.e("Longitude", longitude.toString())
        lifecycleScope.launch {
            homeScreenViewModel.getAllRequest("0", latitude.toString(), longitude.toString())
                .observe(viewLifecycleOwner) { jsonObject ->
                    dismissMe()
                    binding.homeRefresh.isRefreshing = false
                    val jsonObjectData = sessionManager.checkResponse(jsonObject)
                    if (jsonObjectData != null) {
                        try {
                            binding.textNoDataFound.visibility = View.GONE
                            binding.rcvCredits.visibility = View.VISIBLE
                            val requestData = Gson().fromJson(jsonObjectData, RequestListModel::class.java)
                            requestList = requestData.data!!
                            Log.e("RequestData", "$requestList")
                            Log.e("Request List", requestData.data.toString())

                            if (requestList.isNotEmpty()){
                                adapterRequestList = RequestListAdapter(requestList, requireActivity())
                                binding.rcvCredits.adapter = adapterRequestList
                                binding.rcvCredits.layoutManager = LinearLayoutManager(requireContext(), LinearLayoutManager.VERTICAL, false)
                                adapterRequestList.setOnRequestAction(object :
                                    RequestListAdapter.OnRequestAction {
                                    override fun onRequestAction(position: Int, requestId: String, action: String) {
                                        if (binding.tvCredits.text.toString().toInt() > 0) {
                                            requestAction(position, requestId, action)
                                            //sessionManager.alertErrorDialog("ok")
                                        } else {
                                            sessionManager.alertErrorDialog("Please Use Subscription")
                                        }
                                    }
                                })
                                binding.textNoDataFound.visibility = View.GONE
                                binding.rcvCredits.visibility = View.VISIBLE
                            }else{
                                binding.textNoDataFound.visibility = View.VISIBLE
                                binding.rcvCredits.visibility = View.GONE
                            }
                        } catch (e: Exception) {
                            Log.d("@Error","***"+e.message)
                            binding.textNoDataFound.visibility = View.VISIBLE
                            binding.rcvCredits.visibility = View.GONE
                        }
                    } else {
                        binding.textNoDataFound.visibility = View.VISIBLE
                        binding.rcvCredits.visibility = View.GONE
                    }

                }
        }
    }

    // This Function is used for Accept or Deny the request
    @SuppressLint("NotifyDataSetChanged")
    private fun requestAction(position: Int, requestId: String, action: String) {
        lifecycleScope.launch {
            homeScreenViewModel.requestAction(requestId, action)
                .observe(viewLifecycleOwner) { jsonObject ->
                    val jsonObjectData = sessionManager.checkResponse(jsonObject)
                    if (jsonObjectData != null) {
                        try {
                            requestList.removeAt(position)
                            adapterRequestList.notifyDataSetChanged()
                            if (requestList.isNotEmpty()){
                                binding.textNoDataFound.visibility = View.GONE
                                binding.rcvCredits.visibility = View.VISIBLE
                            }else{
                                binding.textNoDataFound.visibility = View.VISIBLE
                                binding.rcvCredits.visibility = View.GONE
                            }
                            if (action.equals("accepted",true)) {
                                val creditvalue = binding.tvCredits.text.toString().toInt() - 1
                                binding.tvCredits.text = creditvalue.toString()
                            }/*else{
                                binding.tvCredits.text.toString()
                            }*/
                        } catch (e: Exception) {
                            Log.d("@Error","***"+e.message)
                        }
                    }
                }
        }
    }


    override fun onResume() {
        super.onResume()
        val activity = requireActivity() as AttronyHomeActivity
        activity.homeResume()

        // Refresh location when fragment resumes
        if (shouldRefreshLocationOnResume) {
            shouldRefreshLocationOnResume = false
            getCredits() // This will eventually call showCurrentLocation()
        }
    }

    private fun handleNewLocation(location: Location) {
        latitude = location.latitude.toString()
        longitude = location.longitude.toString()
        Log.d("checkLocation", "FINAL Location = $latitude , $longitude")
        getAllRequest(location.latitude, location.longitude)
        binding.tvCurrentLocation.text = sessionManager.getAddressFromLocation(location.latitude, location.longitude)
    }

    @Deprecated("Deprecated in Java")
    override fun onRequestPermissionsResult(requestCode: Int, permissions: Array<out String>, grantResults: IntArray) {
        super.onRequestPermissionsResult(requestCode, permissions, grantResults)
        if (requestCode == 100) {
            if (grantResults.isNotEmpty() && (grantResults[0] + grantResults[0] == PackageManager.PERMISSION_GRANTED)) {
                displayLocationSettingsRequest(requireContext())
            } else {
                if (ContextCompat.checkSelfPermission(requireContext(), Manifest.permission.ACCESS_FINE_LOCATION) == PackageManager.PERMISSION_GRANTED) {
                    getCurrentLocation()
                } else {
                    requestPermissions(arrayOf(Manifest.permission.ACCESS_FINE_LOCATION, Manifest.permission.ACCESS_COARSE_LOCATION), 100)
                }
            }
        }
    }

    private fun displayLocationSettingsRequest(context: Context) {
        val googleApiClient = GoogleApiClient.Builder(context)
            .addApi(LocationServices.API).build()
        googleApiClient.connect()
        val locationRequest: LocationRequest = LocationRequest.create()
        locationRequest.priority = LocationRequest.PRIORITY_HIGH_ACCURACY
        locationRequest.interval = 10000
        locationRequest.fastestInterval = 1000
        locationRequest.numUpdates = 1
        val builder = LocationSettingsRequest.Builder().addLocationRequest(locationRequest)
        builder.setAlwaysShow(true)
        val result: PendingResult<LocationSettingsResult> =
            LocationServices.SettingsApi.checkLocationSettings(googleApiClient, builder.build())
        result.setResultCallback { result ->
            val status: Status = result.status
            when (status.statusCode) {
                LocationSettingsStatusCodes.SUCCESS -> {
                    Log.i(tAG, "All location settings are satisfied.")
                    getCurrentLocation()
                }
                LocationSettingsStatusCodes.RESOLUTION_REQUIRED -> {
                    Log.i(tAG, "Location settings are not satisfied. Show the user a dialog to upgrade location settings ")
                    try {
                        // Show the dialog by calling startResolutionForResult(), and check the result
                        // in onActivityResult().
                        status.resolution?.let {
                            startIntentSenderForResult(it.intentSender, 100, null, 0, 0, 0, null)
                        }

                    } catch (e: IntentSender.SendIntentException) {
                        Log.i(tAG, "PendingIntent unable to execute request.")
                    }
                }
                LocationSettingsStatusCodes.SETTINGS_CHANGE_UNAVAILABLE -> Log.i(tAG, "Location settings are inadequate, and cannot be fixed here. Dialog not created.")

            }
        }
    }

    @Deprecated("Deprecated in Java")
    override fun onActivityResult(requestCode: Int, resultCode: Int, data: Intent?) {
        super.onActivityResult(requestCode, resultCode, data)
        if (requestCode == 100) {
            if (Activity.RESULT_OK == resultCode) {
                getCurrentLocation()
            } else {
                Toast.makeText(requireContext(), "Please turn on location", Toast.LENGTH_SHORT).show()
            }
        }
    }

    private fun getCurrentLocation() {
        // Initialize Location manager
        val locationManager = requireActivity().getSystemService(Context.LOCATION_SERVICE) as LocationManager
        // Check condition
        if (locationManager.isProviderEnabled(LocationManager.GPS_PROVIDER) || locationManager.isProviderEnabled(LocationManager.NETWORK_PROVIDER)) {
            // When location service is enabled
            // Get last location
            if (ActivityCompat.checkSelfPermission(
                    requireContext(),
                    Manifest.permission.ACCESS_FINE_LOCATION
                ) != PackageManager.PERMISSION_GRANTED && ActivityCompat.checkSelfPermission(
                    requireContext(),
                    Manifest.permission.ACCESS_COARSE_LOCATION
                ) != PackageManager.PERMISSION_GRANTED
            ) {
                // TODO: Consider calling
                //    ActivityCompat#requestPermissions
                // here to request the missing permissions, and then overriding
                //   public void onRequestPermissionsResult(int requestCode, String[] permissions,
                //                                          int[] grantResults)
                // to handle the case where the user grants the permission. See the documentation
                // for ActivityCompat#requestPermissions for more details.
                return
            }
            mFusedLocationClient.lastLocation.addOnCompleteListener { task ->
                // Initialize location
                val location = task.result
                // Check condition
                if (location != null) {
                    latitude = location.latitude.toString()
                    longitude = location.longitude.toString()
                    handleNewLocation(location)
                } else {
                    val locationRequest =
                        LocationRequest().setPriority(LocationRequest.PRIORITY_HIGH_ACCURACY)
                            .setInterval(10000)
                            .setFastestInterval(1000)
                            .setNumUpdates(1)

                    val locationCallback: LocationCallback = object : LocationCallback() {
                        override fun onLocationResult(locationResult: LocationResult) {
                            // Initialize
                            // location
                            val location1 = locationResult.lastLocation
                            latitude = location1!!.latitude.toString()
                            longitude = location1.longitude.toString()
                            handleNewLocation(location1)
                        }
                    }
                    mFusedLocationClient.requestLocationUpdates(locationRequest, locationCallback, Looper.myLooper()!!)
                }
            }
        } else {
            requestPermissions(
                arrayOf(
                    Manifest.permission.ACCESS_FINE_LOCATION,
                    Manifest.permission.ACCESS_COARSE_LOCATION
                ), 100
            )
        }
    }

    private fun notification() {
        val iconRes = if (notificationStatus == 0) {
            R.drawable.notification_icon
        } else {
            R.drawable.notification_new
        }

        Glide.with(requireActivity())
            .load(iconRes)
            .into(binding.NotifyIcon)
    }

    override fun selectAddress(lat: String, lng: String) {
        getAllRequest(lat.toDouble(), lng.toDouble())
        binding.tvCurrentLocation.text = sessionManager.getAddressFromLocation(lat.toDouble(), lng.toDouble())
    }



