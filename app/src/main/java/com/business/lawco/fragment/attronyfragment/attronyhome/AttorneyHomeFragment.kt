                if (jsonObjectData != null) {
                    try {
                        val data = jsonObjectData.getAsJsonObject("data")
                        binding.tvUserName.text = if (!data.get("name").isJsonNull) data.get("name").asString else null
                    } catch (e: Exception) {
                        Log.d("@Error","***"+e.message)
                    }
                }

            }
        }
        getActivation()
    }

    // This Function is used for get user credit
    private fun getCredits() {
        showMe()
        lifecycleScope.launch {
            homeScreenViewModel.getCredit().observe(viewLifecycleOwner) { jsonObject ->
                dismissMe()
                val jsonObjectData = sessionManager.checkResponse(jsonObject)
                if (jsonObjectData != null) {
                    try {
                        binding.tvCredits.text = jsonObjectData["credit"].asInt.toString()
                        jsonObjectData["notification"].asInt.let {
                            notificationStatus=it
                        }
                    } catch (e: Exception) {
                        notificationStatus=0
                        Log.d("@Error","***"+e.message)
                    }
                }else{
                    notificationStatus=0
                }
                notification()
            }
        }
        getUserProfile()
    }

