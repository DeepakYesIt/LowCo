    }

    override fun onClick(p0: View?) {
        when (p0!!.id) {
            R.id.btnCurrentLocation -> {
                val selectAddressDialog = SelectAddressBottomDialog(this)
                selectAddressDialog.show(childFragmentManager, selectAddressDialog.tag)
            }
            R.id.NotifyIcon -> {
                notificationStatus=0
                notification()
                findNavController().navigate(R.id.notificationsFragment)
            }
            R.id.btActive -> {
                if (binding.btActive.background == null) {
                    if (!latitude.equals("0",true) && !longitude.equals("0",true)) {
                        if (!sessionManager.isNetworkAvailable()) {
                            sessionManager.alertErrorDialog(getString(R.string.no_internet))
                        } else {
                            setActivation(latitude, longitude)
                        }

                    } else {
                        if (!sessionManager.isNetworkAvailable()) {
                            sessionManager.alertErrorDialog(getString(R.string.no_internet))
                        } else {
                            locationData()
                        }
                    }
                }
            }
            R.id.btInactive -> {
                if (binding.btInactive.background == null) {
                    if (!latitude.equals("0",true) && !longitude.equals("0",true)) {
                        setActivation(latitude, longitude)
                    } else {
                        locationData()
                    }
                }
            }
        }
    }

    private fun locationData(){
        if (ContextCompat.checkSelfPermission(requireContext(), Manifest.permission.ACCESS_FINE_LOCATION) == PackageManager.PERMISSION_GRANTED) {
            getCurrentLocation()
        } else {
            requestPermissions(arrayOf(Manifest.permission.ACCESS_FINE_LOCATION, Manifest.permission.ACCESS_COARSE_LOCATION), 100)
        }
    }

    // This Function is used for get User Profile
    private fun getUserProfile() {
        lifecycleScope.launch {
            homeScreenViewModel.getUserProfile().observe(viewLifecycleOwner) { jsonObject ->
                dismissMe()
